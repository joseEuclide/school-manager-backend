package com.escola.dto;

public class TesourariaDTO {
	
	private String mes;
	private double propina;
	private double multaPropina;
	
	
	
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	public double getPropina() {
		return propina;
	}
	public void setPropina(double propina) {
		this.propina = propina;
	}
	public double getMultaPropina() {
		return multaPropina;
	}
	public void setMultaPropina(double multaPropina) {
		this.multaPropina = multaPropina;
	}
	
	
	

}
